namespace Rise.Data.Collections;

/// <summary>
/// Defines possible preferences when sorting a set of data.
/// </summary>
public enum SortDirection
{
    /// <summary>
    /// The data should be sorted in ascending order (a,b,c).
    /// </summary>
    Ascending = 0,

    /// <summary>
    /// The data should be sorted in descending order (c,b,a).
    /// </summary>
    Descending = 1
}
